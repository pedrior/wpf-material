namespace WPF.Material.Controls;

/// <summary>
/// Represents the alignment of the destination items in a <see cref="NavigationRail"/> control.
/// </summary>
public enum NavigationRailAlignment
{
    /// <summary>
    /// Identifies that the destination items are aligned to the top of the <see cref="NavigationRail"/> control.
    /// </summary>
    Top,

    /// <summary>
    /// Identifies that the destination items are aligned to the middle of the <see cref="NavigationRail"/> control.
    /// </summary>
    Middle,

    /// <summary>
    /// Identifies that the destination items are aligned to the bottom of the <see cref="NavigationRail"/> control.
    /// </summary>
    Bottom
}