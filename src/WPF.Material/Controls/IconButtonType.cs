namespace WPF.Material.Controls;

/// <summary>
/// Represents the types of an icon button.
/// </summary>
public enum IconButtonType
{
    /// <summary>
    /// Identifies the standard icon button, which has a transparent background and no border.
    /// </summary>
    Standard,

    /// <summary>
    /// Identifies the filled icon button, which contrasts with the surface color.
    /// </summary>
    Filled,

    /// <summary>
    /// Identifies the tonal icon button, which has a lighter background color and darker label color.
    /// </summary>
    Tonal,

    /// <summary>
    /// Identifies the outlined icon button, which has a transparent background and a border.
    /// </summary>
    Outlined
}