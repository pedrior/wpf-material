namespace WPF.Material.Controls;

/// <summary>
/// Represents the styles of a symbol.
/// </summary>
public enum SymbolStyle
{
    /// <summary>
    /// Identifies the filled symbol style.
    /// </summary>
    Rounded,

    /// <summary>
    /// Identifies the outlined symbol style.
    /// </summary>
    Outlined,

    /// <summary>
    /// Identifies the sharp symbol style.
    /// </summary>
    Sharp
}