namespace WPF.Material.Controls;

/// <summary>
/// Represents the appearances of a FAB.
/// </summary>
public enum FloatingActionButtonAppearance
{
    /// <summary>
    /// Identifies a FAB with a surface appearance.
    /// </summary>
    Surface,
    
    /// <summary>
    /// Identifies a FAB with a primary appearance.
    /// </summary>
    Primary,

    /// <summary>
    /// Identifies a FAB with a secondary appearance.
    /// </summary>
    Secondary,

    /// <summary>
    /// Identifies a FAB with a tertiary appearance.
    /// </summary>
    Tertiary
}