namespace WPF.Material.Components;

/// <summary>
/// Specifies the alignment of the destination items in a <see cref="NavigationRail"/> component.
/// </summary>
public enum NavigationRailAlignment
{
    /// <summary>
    /// Indicates that destination items are aligned to the top of the <see cref="NavigationRail"/>.
    /// </summary>
    Top,

    /// <summary>
    /// Indicates that destination items are aligned to the middle of the <see cref="NavigationRail"/>.
    /// </summary>
    Middle,

    /// <summary>
    /// Indicates that destination items are aligned to the bottom of the <see cref="NavigationRail"/>.
    /// </summary>
    Bottom
}