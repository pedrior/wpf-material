namespace WPF.Material.Components;

/// <summary>
/// Represents the appearances of a button.
/// </summary>
public enum ButtonAppearance
{
    /// <summary>
    /// Identifies a filled button that contrasts with the surface color.
    /// </summary>
    Filled,

    /// <summary>
    /// Identifies a tonal button with a light background and dark label.
    /// </summary>
    Tonal,

    /// <summary>
    /// Identifies an elevated button with a lighter background color and shadow.
    /// </summary>
    Elevated,

    /// <summary>
    /// Identifies an outlined button with a transparent background and border.
    /// </summary>
    Outlined,

    /// <summary>
    /// Identifies a text button with a transparent background and no border.
    /// </summary>
    Text
}