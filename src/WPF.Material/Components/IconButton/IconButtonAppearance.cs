namespace WPF.Material.Components;

/// <summary>
/// Represents the appearance of an icon button.
/// </summary>
public enum IconButtonAppearance
{
    /// <summary>
    /// Identifies the standard icon button with a transparent background and no border.
    /// </summary>
    Standard,

    /// <summary>
    /// Identifies the filled icon button that contrasts with the surface color.
    /// </summary>
    Filled,

    /// <summary>
    /// Identifies the tonal icon button featuring a lighter background and darker label color.
    /// </summary>
    Tonal,

    /// <summary>
    /// Identifies the outlined icon button with a transparent background and border.
    /// </summary>
    Outlined
}