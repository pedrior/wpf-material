namespace WPF.Material.Components;

/// <summary>
/// Specifies the sizes of a <see cref="FloatingActionButton"/>.
/// </summary>
public enum FloatingActionButtonSize
{
    /// <summary>
    /// Indicates a 56x56 FAB.
    /// </summary>
    Standard,

    /// <summary>
    /// Indicates a 40x40 FAB.
    /// </summary>
    Small,

    /// <summary>
    /// Indicates a 96x96 FAB. 
    /// </summary>
    Large
}