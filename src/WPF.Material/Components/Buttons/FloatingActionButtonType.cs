namespace WPF.Material.Components;

/// <summary>
/// Specifies the types of FABs, which determine their appearance.
/// </summary>
public enum FloatingActionButtonType
{
    /// <summary>
    /// Indicates that the FAB uses the surface color.
    /// </summary>
    Surface,

    /// <summary>
    /// Indicates that the FAB uses the primary color.
    /// </summary>
    Primary,

    /// <summary>
    /// Indicates that the FAB uses the secondary color.
    /// </summary>
    Secondary,

    /// <summary>
    /// Indicates that the FAB uses the tertiary color.
    /// </summary>
    Tertiary
}