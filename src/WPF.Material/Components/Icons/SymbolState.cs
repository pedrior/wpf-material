namespace WPF.Material.Components;

/// <summary>
/// Specifies the states associated with a symbol.
/// </summary>
public enum SymbolState
{
    /// <summary>
    /// Indicates no state.
    /// </summary>
    None,
    
    /// <summary>
    /// Indicates a rest symbol.
    /// </summary>
    Rest,
    
    /// <summary>
    /// Indicates a hover symbol.
    /// </summary>
    Hover,
    
    /// <summary>
    /// Indicates a press symbol.
    /// </summary>
    Press,
    
    /// <summary>
    /// Indicates a select symbol.
    /// </summary>
    Select
}