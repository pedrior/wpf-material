namespace WPF.Material.Assists;

/// <summary>
/// Identifies the elevation levels that can be applied to a component.
/// </summary>
public enum ElevationLevel
{
    /// <summary>
    /// Identifies no elevation level (disabled).
    /// </summary>
    None,

    /// <summary>
    /// Identifies the elevation level 1.
    /// </summary>
    Level1,

    /// <summary>
    /// Identifies the elevation level 2.
    /// </summary>
    Level2,

    /// <summary>
    /// Identifies the elevation level 3.
    /// </summary>
    Level3,

    /// <summary>
    /// Identifies the elevation level 4.
    /// </summary>
    Level4,

    /// <summary>
    /// Identifies the elevation level 5.
    /// </summary>
    Level5
}