namespace WPF.Material.Styles;

/// <summary>
/// Represents the styles of a text element.
/// </summary>
public enum TypeStyle
{
    /// <summary>
    /// Identifies the 'display-large' type style.
    /// </summary>
    DisplayLarge,

    /// <summary>
    /// Identifies the 'display-medium' type style.
    /// </summary>
    DisplayMedium,

    /// <summary>
    /// Identifies the 'display-small' type style.
    /// </summary>
    DisplaySmall,

    /// <summary>
    /// Identifies the 'headline-large' type style.
    /// </summary>
    HeadlineLarge,

    /// <summary>
    /// Identifies the 'headline-medium' type style.
    /// </summary>
    HeadlineMedium,

    /// <summary>
    /// Identifies the 'headline-small' type style.
    /// </summary>
    HeadlineSmall,

    /// <summary>
    /// Identifies the 'title-large' type style.
    /// </summary>
    TitleLarge,

    /// <summary>
    /// Identifies the 'title-medium' type style.
    /// </summary>
    TitleMedium,

    /// <summary>
    /// Identifies the 'title-small' type style.
    /// </summary>
    TitleSmall,

    /// <summary>
    /// Identifies the 'subheading-large' type style.
    /// </summary>
    BodyLarge,

    /// <summary>
    /// Identifies the 'subheading-medium' type style.
    /// </summary>
    BodyMedium,

    /// <summary>
    /// Identifies the 'subheading-small' type style.
    /// </summary>
    BodySmall,

    /// <summary>
    /// Identifies the 'caption-large' type style.
    /// </summary>
    LabelLarge,

    /// <summary>
    /// Identifies the 'caption-medium' type style.
    /// </summary>
    LabelMedium,

    /// <summary>
    /// Identifies the 'caption-small' type style.
    /// </summary>
    LabelSmall
}