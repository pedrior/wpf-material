namespace WPF.Material.Styles;

/// <summary>
/// Represents the styles of a shape.
/// </summary>
public enum ShapeStyle
{
    /// <summary>
    /// Indicates that no style is applied to the shape.
    /// </summary>
    None,

    /// <summary>
    /// Indicates a shape with 4px radius corners.
    /// </summary>
    ExtraSmall,

    /// <summary>
    /// Indicates a shape with 8px radius corners.
    /// </summary>
    Small,

    /// <summary>
    /// Indicates a shape with 12px radius corners.
    /// </summary>
    Medium,

    /// <summary>
    /// Indicates a shape with 16px radius corners.
    /// </summary>
    Large,

    /// <summary>
    /// Indicates a shape with 24px radius corners.
    /// </summary>
    ExtraLarge,

    /// <summary>
    /// Indicates a shape with 100% radius corners.
    /// </summary>
    Full
}