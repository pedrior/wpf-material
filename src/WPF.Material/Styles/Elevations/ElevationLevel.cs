namespace WPF.Material.Styles;

/// <summary>
/// Represents the elevation level of a component.
/// </summary>
public enum ElevationLevel
{
    /// <summary>
    /// Indicates the control should have no elevation applied.
    /// </summary>
    None,

    /// <summary>
    /// Identifies the elevation level 1.
    /// </summary>
    Level1,

    /// <summary>
    /// Identifies the elevation level 2.
    /// </summary>
    Level2,

    /// <summary>
    /// Identifies the elevation level 3.
    /// </summary>
    Level3,

    /// <summary>
    /// Identifies the elevation level 4.
    /// </summary>
    Level4,

    /// <summary>
    /// Identifies the elevation level 5.
    /// </summary>
    Level5
}