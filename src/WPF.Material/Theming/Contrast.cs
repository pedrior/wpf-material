namespace WPF.Material.Theming;

/// <summary>
/// Defines contrast levels. Color schemes use this to indicate their contrast.
/// </summary>
public enum Contrast
{
    /// <summary>
    /// Identifies a normal contrast.
    /// </summary>
    Normal,
    
    /// <summary>
    /// Identifies a medium contrast.
    /// </summary>
    Medium,
    
    /// <summary>
    /// Identifies a high contrast.
    /// </summary>
    High
}